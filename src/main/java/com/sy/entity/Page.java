package com.sy.entity;

public class Page {
	 int start;
	    int count ;
	    int last ;
	    
	    public int getStart() {
	        return start;
	    }
	    public void setStart(int start) {
	        this.start = start;
	    }
	    public int getCount() {
	        return count;
	    }
	    public void setCount(int count) {
	        this.count = count;
	    }
	    public int getLast() {
	        return last;
	    }
	    public void setLast(int last) {
	        this.last = last;
	    }
}
